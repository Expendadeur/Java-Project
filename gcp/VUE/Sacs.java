    private void loadData() {
        List<SacInterne> sacsInternes = repository.readAllSacsInternes(); // Appel de la m√©thode

    DefaultTableModel tableModel = (DefaultTableModel) sacs.getModel(); // Remplacez par le nom de votre JTable
    tableModel.setRowCount(0); // Vider le tableau avant d'ajouter de nouvelles donn√©es

    for (SacInterne sac : sacsInternes) {
        Object[] rowData = {
            sac.getIdSacPetitSac(),
            sac.getPoidsSacInterne(),
            sac.getRegime(),
            sac.getRegion(),
            sac.getDetailsSacInterne()
        };
        tableModel.addRow(rowData); // Ajouter chaque sac dans le tableau
    }
    }
    private void updateSacInterne() {
    int selectedRow = sacs.getSelectedRow(); // Obtenir la ligne s√©lectionn√©e
    if (selectedRow >= 0) {
        int idSac = (int) sacs.getValueAt(selectedRow, 4); // Supposons que l'ID est dans la premi√®re colonne
        double Poids = 0.0; // Initialisation du poids
        String poidsText = poids.getText().trim(); // R√©cup√©rer le poids depuis un champ de texte

        // V√©rification du poids
        if (poidsText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un poids valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            try {
                Poids = Double.parseDouble(poidsText); // Conversion du poids
                // V√©rifier si le poids d√©passe 2
                if (Poids > 2) {
                    JOptionPane.showMessageDialog(this, "Le poids ne doit pas d√©passer 2.", "Erreur", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Le poids doit √™tre un nombre valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        String regimes = regime.getText(); // R√©cup√©rer le r√©gime
        String regions = region.getText(); // R√©cup√©rer la r√©gion
        String details = description.getText(); // R√©cup√©rer les d√©tails

        SacInterne sacToUpdate = new SacInterne(idSac, Poids, regimes, regions, details);
        repository.updateSacInterne(sacToUpdate); // Appel de la m√©thode de mise √† jour
        loadData(); // Recharger les sacs apr√®s mise √† jour
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un sac √† mettre √† jour.");
    }
}
    
private void deleteSacInterne() {
    int selectedRow = sacs.getSelectedRow(); // Obtenir la ligne s√©lectionn√©e
    if (selectedRow >= 0) {
        int idSac = (int) sacs.getValueAt(selectedRow, 4); // Supposons que l'ID est dans la premi√®re colonne

        int confirmation = JOptionPane.showConfirmDialog(this, "√ätes-vous s√ªr de vouloir supprimer ce sac ?", "Confirmer", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            repository.deleteSacInterne(idSac); // Appel de la m√©thode de suppression
            loadData(); // Recharger les sacs apr√®s suppression
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un sac √† supprimer.");
    }
}    

