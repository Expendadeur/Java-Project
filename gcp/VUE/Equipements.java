    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String Etat = (String) etat.getSelectedItem();
    String Nom = nomEquip.getText().trim();
    Date dateAchat = dateachat.getDate();
    String Type=type.getText().trim();
    // Validation des champs
    if (Etat == null || Nom.isEmpty() || dateAchat == null) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs obligatoires.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return;
    }
 // Création de l'objet Tarifications
    Equipement tarif = new Equipement(Nom,Type, dateAchat,Etat); 

    // Confirmation de l'ajout
    int confirmation = JOptionPane.showConfirmDialog(
        this, 
        "Êtes-vous sûr de vouloir d'Ajouter cette Equipement ?", 
        "Confirmation", 
        JOptionPane.YES_NO_OPTION
    );  

    if (confirmation == JOptionPane.YES_OPTION) {
        // Appeler la méthode pour ajouter la tarification
        repository.addEquipement(tarif);
        // Charger les détails dans le JTable après l'ajout
        loadData();
        initialiser();
        JOptionPane.showMessageDialog(this, "Equipement ajouté avec succès !");
    } else {
        JOptionPane.showMessageDialog(this, "Ajout annulé.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void equipetbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_equipetbMouseClicked
              if (evt.getClickCount() == 1) { // Vérifie un double clic
     int selectedRow = equipetb.getSelectedRow();
    if (selectedRow >= 0) {
        // Récupérer les données de la ligne sélectionnée
        int id = (int) equipetb.getValueAt(selectedRow, 4);
        String types = (String) equipetb.getValueAt(selectedRow, 1); // Colonne 0 pour nom
        String nom = (String) equipetb.getValueAt(selectedRow, 0); // Colonne 1 pour régime
         String Etat = (String) equipetb.getValueAt(selectedRow, 3); // Colonne 1 pour régime
         
        IdEqu.setText(Integer.toString(id));
        nomEquip.setText(nom); // Conversion de double à String
        etat.setSelectedItem(Etat);
        type.setText(types);
        Date date = (Date) equipetb.getValueAt(selectedRow, 2); // Colonne 2 pour date
        dateachat.setDate(date);
    }
        }
    }//GEN-LAST:event_equipetbMouseClicked

       private void deleteSelectedEquipement() {
        int selectedRow = equipetb.getSelectedRow();
        if (selectedRow != -1) { // Vérifier si une ligne est sélectionnée
            int idEq = (int) equipetb.getValueAt(selectedRow,4); // Récupérer l'ID de l'agence
            int confirmation = JOptionPane.showConfirmDialog(
                null,
                "Êtes-vous sûr de vouloir supprimer cette Equipement ?",
                "Confirmation",
                JOptionPane.YES_NO_OPTION
            );
            if (confirmation == JOptionPane.YES_OPTION) {
                repository.deleteEquipement(idEq); // Appeler la méthode de suppression
                loadData(); // Mettre à jour le tableau après la suppression
                JOptionPane.showMessageDialog(null, "Equipement supprimée avec succès !");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un equipement à supprimer.");
        }
    }
    
