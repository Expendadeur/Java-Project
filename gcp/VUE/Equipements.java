    private void loadData() {
    List<Equipement> equipement = repository.readAllEquipements();
     // Vider le modèle de tableau avant de le remplir
    DefaultTableModel tableModel = (DefaultTableModel) equipetb.getModel();
    tableModel.setRowCount(0); // Vide le tableau
 for (gcp.MODELES.Equipement tat :equipement) {
            Object[] rowData = { 
                tat.getNomEquipement(),
                tat.getTypeEquipement(),
                tat.getDateAchat(),
                tat.getEtat(),
                tat.getIdEquipement()
                
            };
            tableModel.addRow(rowData);
        }
    }

